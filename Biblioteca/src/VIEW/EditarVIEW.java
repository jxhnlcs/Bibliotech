    private void AlterarLivro() {

        if (txtNome.getText().isEmpty() || txtEditora.getText().isEmpty() || txtAutor.getText().isEmpty()
                || txtCategoria.getText().isEmpty() || txtAno.getText().isEmpty() || txtCodlivro.getText().isEmpty()
                || txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            int id_livro;
            String nome_livro, editora, autor, categoria, valor, ano, cod;

            id_livro = Integer.parseInt(txtID.getText());
            valor = txtValor.getText();
            ano = txtAno.getText();
            cod = txtCodlivro.getText();
            nome_livro = txtNome.getText();
            editora = txtEditora.getText();
            autor = txtAutor.getText();
            categoria = txtCategoria.getText();

            LivrosMODEL objlivrosM = new LivrosMODEL();
            objlivrosM.setNomeLivro(nome_livro);
            objlivrosM.setAnoLancamento(ano);
            objlivrosM.setAutor(autor);
            objlivrosM.setCategoria(categoria);
            objlivrosM.setEditora(editora);
            objlivrosM.setId(id_livro);
            objlivrosM.setValorLivro(valor);
            objlivrosM.setCodLivro(cod);

            LivrosDAO objlivrosD = new LivrosDAO();
            objlivrosD.editarLivro(objlivrosM);
            
            JOptionPane.showMessageDialog(null, "Livro editado com sucesso", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
        }
    }

